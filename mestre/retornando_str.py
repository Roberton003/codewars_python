def saudacao(nome):
    """
    Função que retorna uma saudação personalizada.

    Args:
        nome (str): O nome da pessoa a ser saudada.

    Returns:
        str: A saudação personalizada.
    """
    return f"Hello, {nome} how are you doing today?"

print(saudacao("John"))  # "Hello, John how are you doing today?"